package com.ringcentral.definitions;


public class ContactResource {
    /// <summary>
    /// </summary>
    public AccountResource account;

    /// <summary>
    /// </summary>
    public String department;

    /// <summary>
    /// </summary>
    public String email;

    /// <summary>
    /// </summary>
    public String extensionNumber;

    /// <summary>
    /// First name of a contact, for user extensions only
    /// </summary>
    public String firstName;

    /// <summary>
    /// Last name of a contact, for user extensions only
    /// </summary>
    public String lastName;

    /// <summary>
    /// Name of a contact, for non-user extensions
    /// </summary>
    public String name;

    /// <summary>
    /// </summary>
    public String id;

    /// <summary>
    /// </summary>
    public String jobTitle;

    /// <summary>
    /// </summary>
    public PhoneNumberResource[] phoneNumbers;

    /// <summary>
    /// </summary>
    public AccountDirectoryProfileImageResource profileImage;

    /// <summary>
    /// </summary>
    public BusinessSiteResource site;

    /// <summary>
    /// </summary>
    public String status;

    /// <summary>
    /// </summary>
    public String type;
}
