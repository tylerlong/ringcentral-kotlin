package com.ringcentral.definitions;


public class PromptLanguageInfo {
    /// <summary>
    /// Link to a prompt language
    /// </summary>
    public String uri;

    /// <summary>
    /// Internal identifier of a language
    /// </summary>
    public String id;

    /// <summary>
    /// Language name
    /// </summary>
    public String name;

    /// <summary>
    /// Language locale code
    /// </summary>
    public String localeCode;
}
